package com.black.im.model.face

/**
 * 自定义表情属性类
 */
class CustomFace {
    /**
     * 获取表情在asset中的路径
     *
     * @return
     */
    /**
     * 设置表情在asset中的路径
     *
     * @param assetPath
     */
    var assetPath: String? = null
    /**
     * 获取表情名字
     *
     * @return
     */
    /**
     * 设置表情名字
     *
     * @param faceName
     */
    var faceName: String? = null
    /**
     * 获取表情的宽
     *
     * @return
     */
    /**
     * 设置表情的宽
     *
     * @param faceWidth
     */
    var faceWidth = 0
    /**
     * 获取表情的高
     *
     * @return
     */
    /**
     * 设置表情的高
     *
     * @param faceHeight
     */
    var faceHeight = 0

}